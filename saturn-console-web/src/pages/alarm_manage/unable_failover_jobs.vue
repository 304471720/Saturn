<template>
    <div v-loading="loading" element-loading-text="请稍等···">
        <div class="page-container">
            <FilterPageList :data="unableFailoverJobsList" :total="total" :order-by="orderBy" :filters="filters">
                <template slot-scope="scope">
                    <el-form :inline="true" class="table-filter">
                        <el-form-item label="">
                            <el-input placeholder="搜索" v-model="filters.jobName"></el-input>
                        </el-form-item>
                        <el-form-item>
                            <el-button type="primary" icon="el-icon-search" @click="scope.search">查询</el-button>
                        </el-form-item>
                    </el-form>
                    <div class="page-table" v-loading="loading" element-loading-text="请稍等···">
                        <div class="page-table-header">
                            <div class="page-table-header-title"><i class="fa fa-list"></i>无法高可用的作业
                                <el-button type="text" @click="getUnableFailoverJobs"><i class="fa fa-refresh"></i></el-button>
                            </div>
                        </div>
                        <el-table stripe border @sort-change="scope.onSortChange" :data="scope.pageData" style="width: 100%">
                            <el-table-column prop="jobName" label="作业名" sortable>
                                <template slot-scope="scope">
                                    <router-link tag="a" :to="{ name: 'job_setting', params: { domain: scope.row.domainName, jobName: scope.row.jobName } }">
                                        <el-button type="text">
                                            {{scope.row.jobName}}
                                        </el-button>
                                    </router-link>
                                </template>
                            </el-table-column>
                            <el-table-column prop="domainName" label="所属域" sortable>
                                <template slot-scope="scope">
                                    <router-link tag="a" :to="{ name: 'job_overview', params: { domain: scope.row.domainName } }">
                                        <el-button type="text">
                                            {{scope.row.domainName}}
                                        </el-button>
                                    </router-link>
                                </template>
                            </el-table-column>
                            <el-table-column prop="degree" label="域等级" sortable>
                                <template slot-scope="scope">
                                    {{$map.degreeMap[scope.row.degree]}}
                                </template>
                            </el-table-column>
                            <el-table-column prop="jobDegree" label="作业等级" sortable>
                                <template slot-scope="scope">
                                    {{$map.degreeMap[scope.row.jobDegree]}}
                                </template>
                            </el-table-column>
                        </el-table>
                    </div>
                </template>
            </FilterPageList>
        </div>
    </div>
</template>
<script>
export default {
  data() {
    return {
      loading: false,
      unableFailoverJobsList: [],
      filters: {
        jobName: '',
      },
      orderBy: 'jobName',
      total: 0,
    };
  },
  methods: {
    getUnableFailoverJobs() {
      this.loading = true;
      this.$http.get('/console/zkClusters/alarmStatistics/unableFailoverJobs').then((data) => {
        this.unableFailoverJobsList = JSON.parse(data);
        this.total = this.unableFailoverJobsList.length;
      })
      .catch(() => { this.$http.buildErrorHandler('获取异常作业请求失败！'); })
      .finally(() => {
        this.loading = false;
      });
    },
  },
  created() {
    this.getUnableFailoverJobs();
  },
};
</script>
